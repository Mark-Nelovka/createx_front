export default function gerColor(category) {
  switch (category) {
    case "Management":
      return "#5A87FC";

    case "Marketing":
      return "#03CEA4";

    case "HR & Recruting":
      return "#F89828";

    case "Design":
      return "#F52F6E";

    case "Development":
      return "#7772f1";

    default:
      break;
  }
}
